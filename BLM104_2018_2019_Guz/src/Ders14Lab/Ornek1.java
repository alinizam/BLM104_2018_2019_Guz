    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
        EntityManagerFactory emf=Persistence.createEntityManagerFactory("BLM104_2018_2019_GuzPU");
        EntityManager em=emf.createEntityManager();
        Query q=em.createQuery("select b from Birim b");
        em.getTransaction().begin();
        List<Birim> birimler=q.getResultList();
        DefaultListModel dlm=new DefaultListModel();
        for ( Birim b : birimler) {
            
                Path p=Paths.get("c:","dosyalar",b.getBirimAdi());
                if (!Files.exists(p)){
                    Query qSilBirim=em.createQuery("delete from Birim b where b.birimId=:pBirimId");
                    qSilBirim.setParameter("pBirimId", b.getBirimId());
                    qSilBirim.executeUpdate();
                    Query qSilPersonel=em.createQuery("delete from Personel p where p.birimId=:pBirimId");
                    qSilPersonel.setParameter("pBirimId", b.getBirimId());
                    qSilPersonel.executeUpdate();
                }
        }
        jList2.setModel(dlm);
        em.getTransaction().commit();

    }//GEN-LAST:event_jButton5ActionPerformed
    
