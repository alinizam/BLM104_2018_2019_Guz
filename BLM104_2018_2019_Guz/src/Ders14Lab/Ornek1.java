    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        BufferedWriter f=null;
        try {
            // TODO add your handling code here:
            String birimAdi=txtBirim.getText();
            EntityManagerFactory emf=Persistence.createEntityManagerFactory("BLM104_2018_2019_GuzPU");
            EntityManager em=emf.createEntityManager();
            //Birim Id bulma
            Query q=em.createQuery("select b from Birim b where b.birimAdi=:pBirimAdi");
            q.setParameter("pBirimAdi",txtBirim.getText() );
            List<Birim> birimler=q.getResultList();
            int birimID=birimler.get(0).getBirimId();
            
            //Dosya açma
            String dosya="c:\\dosyalar\\"+txtBirim.getText();
            f = new BufferedWriter(new FileWriter(dosya));
            //Personelleri bulma
            Query qPersonel=em.createQuery("select p from Personel p where p.birimId=:pBirimId");
            qPersonel.setParameter("pBirimId", birimID);
            List<Personel> calisanlar=qPersonel.getResultList();
            for (Personel c : calisanlar) {
                f.write(c.getAdi()+" "+c.getSoyadi());
                f.newLine();
            }
        } catch (IOException ex) {
            Logger.getLogger(Ornek1.class.getName()).log(Level.SEVERE, null, ex);
        } finally {
            try {
                f.close();
            } catch (IOException ex) {
                Logger.getLogger(Ornek1.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

